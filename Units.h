#ifndef MAIN_CPP_UNITS_H
#define MAIN_CPP_UNITS_H

double d = 2.; //scale by 10
double m = 3.; //meter
double kg = 5.; //kilogram
double s = 7.; //second
double K = 11.; //kelvin
double A = 13.; //amp
double mol = 17.; //mol
double cd = 19.; //candelas

double N = m*kg/s/s; //Newtons
double Hz = 1./s; //Hertz
double Pa = N/m/m; //Pascal
double J = N*m; //Joules
double W = J/s; //Watt
double C = s*A; //Coulomb
double V = W/A; //Volt
double ohm = V/A; //ohm


#endif //MAIN_CPP_UNITS_H
